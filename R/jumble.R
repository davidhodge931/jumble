#' A discrete colour palette
#'
#' @description
#' A pretty discrete 5 colour palette that is relatively colourblind and contrast safe on a light background.
#'
#' @format NULL
#' @return A character vector.
#' @export
#'
#' @seealso [pink] [slate]
#'
#' @examples
#' scales::show_col(jumble)
jumble <- c(
  "#0095A8FF",
  "#FFA600FF",
  "#003F5CFF",
  "#DA3C39FF",
  "#CDC5BFFF"
)

#' Teal colour
#' @export
#' @examples
#' scales::show_col(teal)
teal <- "#0095A8FF"

#' Orange colour
#' @export
#' @examples
#' scales::show_col(orange)
orange <- "#FFA600FF"

#' Navy colour
#' @export
#' @examples
#' scales::show_col(navy)
navy <- "#003F5CFF"

#' Red colour
#' @export
#' @examples
#' scales::show_col(red)
red <- "#DA3C39FF"

#' grey colour
#' @export
#' @examples
#' scales::show_col(grey)
grey <- "#CDC5BFFF"

#' Pink colour
#'
#' @description
#' Can be used with jumble, but not colourblind safe.
#'
#' @export
#' @examples
#' scales::show_col(pink)
pink <- "#EC9ECBFF"

#' Slate colour
#'
#' @description
#' Can be used with jumble, but not colourblind safe.
#'
#' @export
#' @examples
#' scales::show_col(slate)
slate <- "#8991A1FF"

